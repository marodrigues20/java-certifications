package chapter_3.sort.comparator;

public class Squirrel {

    private int weight;
    private String species;

    public Squirrel(int weight, String species) {
        this.weight = weight;
        this.species = species;
    }

    public int getWeight() {
        return weight;
    }

    public String getSpecies() {
        return species;
    }
}
